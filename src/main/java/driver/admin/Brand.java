package driver.admin;

public class Brand {
    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void display() {
        System.out.println("Brand Id: " + id);
        System.out.println("Brand Name: " + name);
    }
}
